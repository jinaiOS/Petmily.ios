import UIKit

enum ThemeColor { }

extension ThemeColor {
    static let white: UIColor = .white
    static let black: UIColor = .black
    static let label: UIColor = .label
    static let lightGray: UIColor = .lightGray
    static let darkGray: UIColor = .darkGray
}

extension ThemeColor {
    static let systemGray: UIColor = .systemGray
    static let systemGray5: UIColor = .systemGray5
    static let systemBackground: UIColor = .systemBackground
}

extension ThemeColor {
    static let appPink = UIColor(hexString: "FFB6C7")
    static let lightPink = UIColor(hexString: "FFB9B9")
    static let deepPink = UIColor(hexString: "FF7373")
    static let mediumGray = UIColor(hexString: "525252")
}
